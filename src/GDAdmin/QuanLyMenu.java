    public void refresh() {
        dtm.setNumRows(0);
        try {
            List<Menu> listMenu = new ArrayList<>();
            listMenu = mc.getListMenu();
            for (Menu m : listMenu) {
                dtm.addRow(new Object[]{
                    m.getMaDoAn(),
                    m.getTenDoAn(),
                    m.getLoaiDoAn(),
                    m.getGiaThanh()});
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

