    public void refresh() {
        dtm.setNumRows(0);
        try {
            List<DangNhap> listDangNhap = new ArrayList<>();
            listDangNhap = mlg.getListLogin();
            for (DangNhap dn : listDangNhap) {
                dtm.addRow(new Object[]{
                    dn.getTaiKhoan(),
                    dn.getMatKhau(),
                    dn.getSoDienThoai(),
                    dn.getHoTen()
                });

            }
        } catch (Exception e) {
            e.printStackTrace();

        }
    }
